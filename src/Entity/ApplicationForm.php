namespace App\Entity;

class ApplicationForm
{
    /**
     * @var integer|null
     */
    private $id;

    /**
     * @return int|null
     */
    public function getId(): ?int
    {
        return $this->id;
    }

    /**
     * @param int|null $id
     * @return ApplicationForm
     */
    public function setId(?int $id): ApplicationForm
    {
        $this->id = $id;
        return $this;
    }
}